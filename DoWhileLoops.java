package thenewboston;

public class DoWhileLoops {
	public static void main (String [] args) {
		
		int counter = 0;					// Define a counter starts at 0
		
		do{
			System.out.println(counter);	// Print out the counter to the console
			counter++;						// Increment the counter by +1
		} while(counter <= 10);				// Run the do statement until it hits 10 
		
	}
}
